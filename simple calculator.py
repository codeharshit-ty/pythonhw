def add(x,y):
    return(x+y)
def subtract(x,y):
    return(x-y)
def multiply(x,y):
    return(x*y)
def divide(x,y):
    return(x/y)
num1=int(input("enter a number"))
num2=int(input("enter a number"))
print("sum :",add(num1,num2))
print("subtract :",subtract(num1,num2))
print("multiply :",multiply(num1,num2))
print("divide :",divide(num1,num2))